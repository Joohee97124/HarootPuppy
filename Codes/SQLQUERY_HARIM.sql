FROM TBL_PARTICIPANTS S,
(
    SELECT ROWNUM AS NUMS, PARTICIPANTS_CODE
    FROM TBL_PARTICIPANTS
)R
WHERE S.PARTICIPANTS_CODE = R.PARTICIPANTS_CODE
  AND R.NUMS=(SELECT MAX(ROWNUM)
