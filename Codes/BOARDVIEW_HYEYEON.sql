-- 찐 BOARDVIEW 코드
CREATE OR REPLACE VIEW BOARDVIEW
AS
SELECT N.NOTICE_CODE AS BOARD_CODE
     , N.NOTICE_WRITER AS BOARD_WRITER
     , A.ADMIN_NAME AS BOARD_NICKNAME
     , N.NOTICE_HEAD_CODE AS BOARD_CATE_CODE
     , N.NOTICE_TITLE AS BOARD_TITLE
     , N.NOTICE_CONTENT AS BOARD_CONTENT
     , N.NOTICE_PHOTO AS BOARD_PHOTO
     , N.NOTICE_VIEW AS BOARD_VIEW
     , N.NOTICE_LIKE AS BOARD_LIKE
     , TO_CHAR(N.NOTICE_DATE, 'YYYY-MM-DD HH24:MI:SS') AS BOARD_CONTENT_DATE
     , (SELECT NOTICE_CATE_CONTENT
        FROM TBL_NOTICE_LEG
        WHERE NOTICE_CATE_CODE = N.NOTICE_HEAD_CODE) AS BOARD_CATE_CONTENT
FROM TBL_ADMIN A
LEFT OUTER JOIN TBL_NOTICE N
ON N.NOTICE_WRITER = A.ADMIN_CODE
UNION ALL
SELECT B.BOARD_CODE AS BOARD_CODE
     , B.BOARD_WRITER AS BOARD_WRITER
     , MEM_NICKNAME AS BOARD_NICKNAME
     , B.BOARD_CATE_CODE AS BOARD_CATE_CODE
     , B.BOARD_TITLE AS BOARD_TITLE
     , B.BOARD_CONTENT AS BOARD_CONTENT
     , B.BOARD_PHOTO AS BOARD_PHOTO
     , B.BOARD_VIEW AS BOARD_VIEW
     , B.BOARD_LIKE AS BOARD_LIKE
     , TO_CHAR(B.BOARD_DATE, 'YYYY-MM-DD HH24:MI:SS') AS BOARD_CONTENT_DATE
     , (SELECT BOARD_CATE_CONTENT
        FROM TBL_BOARD_LEG
        WHERE BOARD_CATE_CODE = B.BOARD_CATE_CODE) AS BOARD_CATE_CONTENT
FROM TBL_BOARD B
LEFT OUTER JOIN TBL_SID S
ON B.BOARD_WRITER = S.SID_CODE
LEFT OUTER JOIN TBL_MEMBER M
ON S.MEM_CODE = M.MEM_CODE
WHERE B.BOARD_CODE NOT IN (SELECT BR.BOARD_CODE 
                           FROM TBL_REPORT_LOG L, TBL_BOARD_REPORT BR
                           WHERE L.REP_LOG_CODE = BR.REP_LOG_CODE
                           AND L.REP_STATE_CODE NOT IN 2
                           GROUP BY BR.BOARD_CODE)
ORDER BY BOARD_WRITER, BOARD_CONTENT_DATE                         
;



SELECT CATE_CONTENT, NUM, TITLE, NICKNAME, CONTENT_DATE, VIEWNUM, LIKENUM
FROM BOARDVIEW;

